#include<iostream>              
using namespace std;             

int main(){                      // Main function starts
    int age;                     // Variable to store age
    cout << "hello\n";           // Prints hello on the screen
    cout << "enter your age:";   // Asks user to enter age
    cin >> age;                  // Takes age input from user
    cout << "your age is:" << age; // Displays the entered age
}       

